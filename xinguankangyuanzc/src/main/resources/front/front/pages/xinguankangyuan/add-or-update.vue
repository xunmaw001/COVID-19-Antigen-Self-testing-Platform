    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					xinguankangyuanUuidNumber: true,
					xinguankangyuanName: false,
					xinguankangyuanPhoto: false,
					xinguankangyuanTypes: false,
