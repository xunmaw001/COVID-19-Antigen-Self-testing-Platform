            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let xinguankangyuanTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'xinguankangyuan_types',
            }
			let xinguankangyuanTypes = await this.$api.page(`dictionary`, xinguankangyuanTypesParams);
			this.xinguankangyuanTypesOptions = xinguankangyuanTypes.data.list
		/*下拉框*/
			let shangxiaTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shangxia_types',
            }
			let shangxiaTypes = await this.$api.page(`dictionary`, shangxiaTypesParams);
			this.shangxiaTypesOptions = shangxiaTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`xinguankangyuan`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.xinguankangyuanId){
                this.ruleForm.xinguankangyuanId = options.xinguankangyuanId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            xinguankangyuanTypesChange(e) {
                this.xinguankangyuanTypesIndex = e.target.value
                this.ruleForm.xinguankangyuanValue = this.xinguankangyuanTypesOptions[this.xinguankangyuanTypesIndex].indexName
                this.ruleForm.xinguankangyuanTypes = this.xinguankangyuanTypesOptions[this.xinguankangyuanTypesIndex].codeIndex
            },
            shangxiaTypesChange(e) {
                this.shangxiaTypesIndex = e.target.value
                this.ruleForm.shangxiaValue = this.shangxiaTypesOptions[this.shangxiaTypesIndex].indexName
                this.ruleForm.shangxiaTypes = this.shangxiaTypesOptions[this.shangxiaTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			xinguankangyuanPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.xinguankangyuanPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.xinguankangyuanUuidNumber)) {
                    this.$utils.msg(`新冠抗原编号不能为空`);
                    return
                }
				if ((!this.ruleForm.xinguankangyuanName)) {
                    this.$utils.msg(`新冠抗原名称不能为空`);
                    return
                }
				if ((!this.ruleForm.xinguankangyuanPhoto)) {
                    this.$utils.msg(`新冠抗原照片不能为空`);
                    return
                }
				if ((!this.ruleForm.xinguankangyuanKucunNumber) && (!this.$validate.isIntNumer(this.ruleForm.xinguankangyuanKucunNumber)) && this.ruleForm.xinguankangyuanKucunNumber >0) {
                    this.$utils.msg(`新冠抗原库存不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.xinguankangyuanClicknum) && (!this.$validate.isIntNumer(this.ruleForm.xinguankangyuanClicknum)) && this.ruleForm.xinguankangyuanClicknum >0) {
                    this.$utils.msg(`新冠抗原热度不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`xinguankangyuan`, this.ruleForm);
                } else {
                    await this.$api.save(`xinguankangyuan`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
